package com.example.calculatorgui.controller;

public class FormData {
    private String from;
    private String to;
    private String value;

    private String resultWeight;
    private String resultVolume;
    private String resultDistance;

    public String getFrom() {
        return from;
    }

    public void setFrom(String from) {
        this.from = from;
    }

    public String getTo() {
        return to;
    }

    public void setTo(String to) {
        this.to = to;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

    public String getResultWeight() {
        return resultWeight;
    }

    public void setResultWeight(String resultWeight) {
        this.resultWeight = resultWeight;
    }

    public String getResultVolume() {
        return resultVolume;
    }

    public void setResultVolume(String resultVolume) {
        this.resultVolume = resultVolume;
    }

    public String getResultDistance() {
        return resultDistance;
    }

    public void setResultDistance(String resultDistance) {
        this.resultDistance = resultDistance;
    }
}
